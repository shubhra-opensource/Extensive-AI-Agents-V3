/**
 * Reads GEMINI_API_KEY or GOOGLE_API_KEY from ../.env and writes ../env-config.js.
 * Run: node scripts/write-env-config.cjs
 */
const fs = require("fs");
const path = require("path");

const extRoot = path.join(__dirname, "..");
const envPath = path.join(extRoot, ".env");
const outPath = path.join(extRoot, "env-config.js");

function parseEnvFile(text) {
  const out = {};
  if (!text) return out;
  const normalized = text.replace(/^\uFEFF/, "");
  for (const line of normalized.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    out[key] = val;
  }
  return out;
}

let key = "";
if (fs.existsSync(envPath)) {
  const parsed = parseEnvFile(fs.readFileSync(envPath, "utf8"));
  key = (parsed.GEMINI_API_KEY || parsed.GOOGLE_API_KEY || "").trim();
}

const banner =
  "/* Generated by scripts/write-env-config.cjs — do not commit if this contains a real key. */\n";
const body = `${banner}var GEMINI_API_KEY_FROM_ENV = ${JSON.stringify(key)};\n`;

fs.writeFileSync(outPath, body, "utf8");
console.log(
  key
    ? `Wrote ${path.relative(process.cwd(), outPath)} (${key.length} chars).`
    : `Wrote ${path.relative(
        process.cwd(),
        outPath
      )} with empty key — add GEMINI_API_KEY or GOOGLE_API_KEY to .env and run again.`
);
